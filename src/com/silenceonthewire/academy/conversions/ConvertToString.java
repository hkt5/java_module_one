package com.silenceonthewire.academy.conversions;

import java.util.Date;

public class ConvertToString {

	public String intToString(int intValue) {
		String currentString = String.valueOf(intValue);
		return currentString;
	}
	
	public String doubleToString(double doubleValue) {
		String currentString = String.valueOf(doubleValue);
		return currentString;
	}
	
	public String booleanToString(boolean boleanValue) {
		String currentString = String.valueOf(boleanValue);
		return currentString;
	}
	
	public String dateToString(Date date) {
		String currentString = String.valueOf(date);
		return currentString;
	}
	
}
